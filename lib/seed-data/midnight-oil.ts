/**
 * Get total song count across all albums.
 */
export function getTotalSongCount(): number {
  return MIDNIGHT_OIL_ALBUMS.reduce((sum, a) => sum + a.tracks.length, 0);
}

/**
 * Flatten all albums into a list of song objects ready for import.
 */
export function getAllSongs(): Array<{
  title: string;
  artist: string;
  album: string;
  year: number;
  track_number: number;
}> {
  const songs: Array<{
    title: string;
    artist: string;
    album: string;
    year: number;
    track_number: number;
  }> = [];

  for (const album of MIDNIGHT_OIL_ALBUMS) {
    for (let i = 0; i < album.tracks.length; i++) {
      songs.push({
        title: album.tracks[i],
        artist: MIDNIGHT_OIL_ARTIST,
        album: album.album,
        year: album.year,
        track_number: i + 1,
      });
    }
  }

  return songs;
}
